//! 16x16 pixel-art icons, embedded as bitmaps.
//!
//! Each icon is a 16x16 grid where 1 = foreground, 0 = transparent.
//! Rendered with embedded-graphics Bitmap.

/// Raw 16x16 monochrome bitmap (2 bytes per row, 32 bytes total).
/// Stored as raw bytes — drawn by components::draw_icon_colored.
pub struct Icon {
    pub data: &'static [u8],
}

macro_rules! icon_16x16 {
    ($($row:expr),+ $(,)?) => {{
        Icon {
            data: &[
                $( ($row >> 8) as u8, ($row & 0xFF) as u8, )+
            ],
        }
    }};
}

